import type { TrendSignal, TrendSignalSource } from '../types'

const SOURCES: readonly TrendSignalSource[] = [
  'HackerNews',
  'GitHub',
  'Reddit',
  'GoogleTrends',
  'ProductHunt',
]

function makeSignals(
  trendId: string,
  scores: readonly [number, number, number, number, number],
  velocities: readonly [number, number, number, number, number],
  baseDate: string,
): readonly TrendSignal[] {
  return SOURCES.map((source, i) => ({
    id: `sig-${trendId}-${source.toLowerCase()}`,
    trend_id: trendId,
    source,
    signal_score: scores[i],
    velocity: velocities[i],
    raw_data: { sample_count: scores[i] * 12, indexed_at: baseDate },
    created_at: baseDate,
  }))
}

